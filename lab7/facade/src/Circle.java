/**
 * The Circle class implements the Shape interface to represent a circle.
 * This class provides functionality to draw a circle.
 *
 * @author yashu
 * @version 1.0
 */
public class Circle implements Shape {
    /**
     * Draws a circle and returns a confirmation message.
     *
     * @return A string indicating that a circle has been drawn.
     */
    public String draw(){
        System.out.println("circle is drawn");
        return "circle is drawn" ;
    }
}
